package Challenges;

public class Pattern {
public static void main(String[] args) {
	System.out.println("Right Half Pyramid");
	System.out.println("*");
	System.out.println("**");
	System.out.println("***");
	System.out.println("****");
	System.out.println("*****");
	System.out.println("-----------------");
	
	System.out.println("Reverse Half Pyramid");
	System.out.println("*****");
	System.out.println("****");
	System.out.println("***");
	System.out.println("**");
	System.out.println("*");
	System.out.println("-----------------");
	
	System.out.println("Left Half Pyramid");
	System.out.println("    *");
	System.out.println("   **");
	System.out.println("  ***");
	System.out.println(" ****");
	System.out.println("*****");
	}
}
