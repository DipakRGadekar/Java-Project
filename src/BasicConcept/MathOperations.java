package BasicConcept;

public class MathOperations {

	public static void main(String[] args) {

		System.out.println("Absolute value " + Math.abs(-15));
		System.out.println("Round up: " + Math.ceil(12.44444));
		System.out.println("Round Down: " + Math.floor(12.44444));
		System.out.println("Round to nearest integer number: " + Math.round(12.6666));
		System.out.println("Max number : " + Math.max(10, 35));
		System.out.println("min number : " + Math.min(10, 35));
		System.out.println("Power calculation: " + Math.pow(10, 30));
		System.out.println("Random number generation: " + Math.random());
		System.out.println("Square root of: " + Math.sqrt(356));
		System.out.println("Pi " + Math.PI);
	}

}
